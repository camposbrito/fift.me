                <h4>Artigo 28 Das disposições sobre utilização da Lavanderia:</h4>

                I. O uso da Lavanderia é destinado aos moradores residentes do edifício;<br>
                II. Os moradores estão autorizados a utilizar a lavanderia do seu respectivo bloco;<br>
                III. O tempo máximo por ciclo e por máquina será de 180 minutos (3hrs) lavagem e secagem.<br>
                IV. Foi aprovado em assembleia que os condôminos inadimplentes estarão impedidos de fazer uso das máquinas.<br>
                V. A tarifação é: Lavadora R$ 0,04 por minuto = R$ 7,20 período de 180 minutos; e Secagem R$ 0,02 por minuto = R$ 3,60 período de 180 minutos.<br>
                VI. O morador deve finalizar o ciclo de utilização após o término do ciclo, desta forma a tarifação ocorrerá sobre o tempo utilizado (Caso o ciclo não seja finalizado a tarifação será sobre o período de 180 minutos).<br>
                VII. A cobrança será incluída no boleto do condomínio.<br>
                VIII. O pagamento da mensalidade não isenta o morador de ressarcir o condomínio por qualquer dano gerado às maquinas de lavar, maquinas de secar, tanques, torneiras, rede hidráulica e rede elétrica;<br>
                IX. Morador que desejar receber o relatório de utilização deve se cadastrar junto com a administradora.<br>
                X. O horário para utilização da lavanderia é das 08:00 horas às 22:00 horas;<br>
                XI. Proibido a utilização para lavagem de roupa de animais.<br>
                <br>
                <h4>Das proibições – É terminantemente proibido</h4>

                Fica proibido o uso da lavanderia por pessoas estranhas ao Condomínio (colegas, amigos, parentes, etc.) que não sejam moradores ou funcionários de moradores do edifício, sem autorização da Administração.
                Deixar e/ou guardar qualquer peça de roupa quer seja por esquecimento ou não na lavanderia, após o horário de uso ;
                Usar a lavanderia para lavagem de peças de terceiros (profissionais de pessoas jurídicas, amigos, parentes, entre outras pessoas);
                Usar a área livre da Lavanderia para encontros sociais e comemorativos;
                Conversas em alto som, na área restrita ou área livre, que atrapalhe o sossego de moradores vizinhos à Lavanderia;
                Mexer nos quadros de energia e instalações Elétricas, e ou hidráulica.

                <h4>Das penalidades</h4>

                Obrigam-se os moradores e a administração condominial cumprirem e fazerem cumprir os direitos e deveres estabelecidos na Convenção de Condomínio, neste Regimento Interno e no Regulamento da Lavanderia:

                Ao morador que permitir o acesso de pessoas estranhas à lavanderia caberá a aplicação de multa equivalente a 100% da taxa padrão, combinada com a suspensão do direito de uso da lavanderia por período de até 1 (um) ano, respeitado o contraditório, sem prejuízo dos efeitos penais e civis aos demais infratores.
                Nas mesmas penas da alínea anterior incorrem os moradores que utilizarem a lavanderia para lavagens de peças de terceiros (profissionais de pessoas jurídicas, amigos, parentes, etc.), sem autorização da Administração;
                Pelo recolhimento e guarda de roupas deixadas pelos usuários na lavanderia, após o horário de uso agendado, será cobrada taxa de serviço cujo valor será definido em reunião de Assembléia;
                A cobrança da taxa se dará junto da Taxa Ordinária de Condomínio.

